    public boolean validar(){
        
            boolean error=false;
           this.usuario.setForeground(Color.black); 
        if (this.usuario.getText().isEmpty()){
            this.usuario.setForeground(Color.red);
             error=true;
        }
        
        this.password.setForeground(Color.red); 
        
        if ( (new String(this.password.getPassword())).isEmpty()){
            this.password.setForeground(Color.red);
             error=true;
        }
        
        return !error;
    }
    
    
